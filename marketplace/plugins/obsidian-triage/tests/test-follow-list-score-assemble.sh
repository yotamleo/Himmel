agent-harness, orchestration, and second-brain signal. Regenerated by
`follow-list-score.mjs assemble` from judge-scored dossiers; do not
hand-edit the tier sections above this line.

Related: [[SomeMOC]]
EOF

cp "$list_path" "$tmpdir/original-list.md"

