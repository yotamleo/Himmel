# -- Test 2: list content -- tier placement, whitelist override, stale entry gone
echo "Test 2: list content"

ORIG_LIST="$tmpdir/original-list.md" NEW_LIST="$list_path" node -e '
const fs = require("fs");
const orig = fs.readFileSync(process.env.ORIG_LIST, "utf8");
const updated = fs.readFileSync(process.env.NEW_LIST, "utf8");

function frontmatterBlock(text) {
  const lines = text.split("\n");
  if (lines[0] !== "---") return null;
  for (let i = 1; i < lines.length; i++) {
    if (lines[i] === "---") return lines.slice(0, i + 1).join("\n");
  }
  return null;
}
function footerBlock(text) {
  const idx = text.indexOf("> **correction:**");
  return idx === -1 ? null : text.slice(idx);
}

console.log("FM_MATCH=" + (frontmatterBlock(orig) === frontmatterBlock(updated)));
console.log("FOOTER_MATCH=" + (footerBlock(orig) === footerBlock(updated)));
console.log("BLOCKQUOTE_PRESENT=" + updated.includes("> **correction:** operator override note that must survive assemble byte-identical."));
console.log("DASHES_PRESENT=" + /\n---\n/.test(updated));
console.log("FOOTER_PROSE_PRESENT=" + updated.includes("**Why this list exists:** this list curates X/Twitter accounts"));
console.log("RELATED_PRESENT=" + updated.includes("Related: [[SomeMOC]]"));
console.log("OLD_GONE=" + !updated.includes("@old"));
console.log("HAS_ALICE=" + updated.includes("[@alice]"));
console.log("HAS_BOB=" + updated.includes("[@bob]"));
console.log("HAS_THEO=" + updated.includes("[@theo]"));
console.log("HAS_CYRIL=" + updated.includes("[@examplehandle]"));
console.log("HAS_DAVE=" + updated.includes("[@dave]"));

const tier3Idx = updated.indexOf("## Tier 3");
const excludedIdx = updated.indexOf("## Excluded");
const cyrilIdx = updated.indexOf("[@examplehandle]");
console.log("CYRIL_IN_TIER3=" + (tier3Idx !== -1 && excludedIdx !== -1 && cyrilIdx > tier3Idx && cyrilIdx < excludedIdx));
const cyrilLine = updated.split("\n").find(l => l.includes("[@examplehandle]"));
console.log("CYRIL_HAS_OVERRIDE=" + (!!cyrilLine && /override/i.test(cyrilLine)));

const daveIdx = updated.indexOf("[@dave]");
console.log("DAVE_IN_EXCLUDED=" + (excludedIdx !== -1 && daveIdx > excludedIdx));
' > "$tmpdir/list-check.txt"

