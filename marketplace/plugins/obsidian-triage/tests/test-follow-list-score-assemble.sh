#!/usr/bin/env bash
# Tests for HIMMEL-660 follow-list-score.mjs — Task 7 `assemble` subcommand.
# Reads <handle>.judgment.json + dossier JSON fixtures written directly to
# disk (no live judge pass), ranks via follow-score.mjs's rankAccounts
# against the real ./follow-overrides.json seed ({whitelist:["examplehandle"],
# exclude:[]}), and asserts the regenerated ai-x-follow-list.md +
# ai-x-follow-scores.md. Hermetic: no network. Cross-platform: bash on
# Linux/macOS/Git-Bash. Uses node (not bun) for CI.

set -u -o pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
TOOLS_DIR="$(cd "$SCRIPT_DIR/../tools" && pwd)"
CLI="$TOOLS_DIR/follow-list-score.mjs"

tmpdir="$(mktemp -d)"
trap 'rm -rf "$tmpdir"' EXIT

pass=0
fail=0
assert() {
    local desc="$1" expected="$2" actual="$3"
    if [ "$expected" = "$actual" ]; then
        echo "  PASS  $desc"; pass=$((pass+1))
    else
        echo "  FAIL  $desc"; echo "         expected: $expected"; echo "         actual:   $actual"; fail=$((fail+1))
    fi
}

node --check "$CLI" 2>/dev/null && s=ok || s=fail
assert "follow-list-score.mjs parses" ok "$s"

# -- fixture vault: existing list file (frontmatter + stale Tier 1 + footer) -
vault="$tmpdir/vault"
mkdir -p "$vault/.obsidian" "$vault/30-Resources/.follow-scores"

list_path="$vault/30-Resources/ai-x-follow-list.md"
scorecard_path="$vault/30-Resources/ai-x-follow-scores.md"

cat > "$list_path" <<'EOF'
---
title: "AI X Follow List"
updated: 2026-06-01
---

