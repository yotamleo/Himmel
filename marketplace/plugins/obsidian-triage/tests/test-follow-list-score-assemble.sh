assert "second assemble run exits 0" 0 "$r2"

sha_after="$(node -e 'const c=require("fs").readFileSync(process.argv[1]);console.log(require("crypto").createHash("sha256").update(c).digest("hex"))' "$list_path")"

assert "re-running assemble yields a byte-identical ai-x-follow-list.md (sha256)" "$sha_before" "$sha_after"

# -- Results summary -----------------------------------------------------
total=$((pass + fail))
echo ""
echo "Results: $pass / $total passed, $fail failed."
